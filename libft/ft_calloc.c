#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*str;

	str = malloc(count * size);
	if (!str)
	{
		exit(EXIT_FAILURE);
		return (NULL);
	}
	ft_bzero(str, size * count);
	return (str);
}
